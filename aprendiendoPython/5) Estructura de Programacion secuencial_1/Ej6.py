#6) Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor 
#por hora.

cantHorasTrab = int(input("\nIngresar la cantidad de horas trabajadas: "))
valorPorHoras = int(input("\nIngresar el valor por hora: "))

sueldo = cantHorasTrab * valorPorHoras

print("El sueldo mensual es: ", sueldo)

